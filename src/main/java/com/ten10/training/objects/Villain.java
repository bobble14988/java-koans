package com.ten10.training.objects;

public class Villain {

  private String name;
  private int strength;

  public Villain(String name, int strength) {
    this.name = name;
    this.strength = strength;
  }

  public String getName() {
    return this.name;
  }

  public int getStrength() {
    return this.strength;
  }
}
